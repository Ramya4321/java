package p1;
public class Arithmetic
{
	public void addition(int a, int b)
	{
		System.out.println(a+b);
	}
	public void sub(int a, int b)
	{
		System.out.println(a-b);
	}
	public void division(int a, int b)
	{
		System.out.println(a/b);
	}
	public void mul(int a, int b)
	{
		System.out.println(a*b);
	}
}